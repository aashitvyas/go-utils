func main() {
	var kubeconfig *string
	if home := homedir.HomeDir(); home != "" {
		kubeconfig = flag.String("kubeconfig", filepath.Join(home, ".kube", "config"), "(optional) absolute path to the kubeconfig file")
	} else {
		kubeconfig = flag.String("kubeconfig", "", "absolute path to the kubeconfig file")
	}
	flag.Parse()

	// use the current context in kubeconfig
	config, err := clientcmd.BuildConfigFromFlags("", *kubeconfig)
	if err != nil {
		panic(err.Error())
	}

	// create the clientset
	clientset, err := kubernetes.NewForConfig(config)
	if err != nil {
		panic(err.Error())
	}

	sambapassword := GetSecret("tenant-purewebash-ash", "app-server-login", "SAMBA_PASSWORD", clientset)
	sambausername := GetSecret("tenant-purewebash-ash", "app-server-login", "SAMBA_USERNAME", clientset)
	fmt.Println(sambapassword)
	fmt.Println(sambausername)

}

func GetSecret(ns, secretname, secret string, c kubernetes.Interface) string {
	secrets, err := c.CoreV1().Secrets(ns).Get(context.TODO(), secretname, metav1.GetOptions{})
	if err != nil {
		panic(err.Error())
	}

	return (string(secrets.Data[secret]))
}
